package chap_01;

public class _02_DataTypes {
//  psvm -> public static void main(String[] args) {} 이것을 자동으로 만들어주는 단축키
  public static void main(String[] args) {
//    sout -> Sytem.out.println(); 이것을 자동으로 만들어주는 단축키
    System.out.println("Hello World"); // string
    System.out.println("안녕하세요?"); // string
    System.out.println(12); // number
    System.out.println(-34); // number
    System.out.println(3.14); // number
    System.out.println(true); // boolean type
    System.out.println(false); // boolean type

    System.out.println(123 + 345); // 자바는 연산기능도 지원
  }
}